    public void mostrarDatos(){
        String titulos[]={"Codigo","Cedula","Nombre","Apellido","Celular","Mail","Titulo","Estado"};
        String registro[]=new String [8];
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        String SQL="select * from profesor";
        try{
            Statement st =(Statement) con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            while(rs.next()){
                registro[0]= rs.getString("profe_codigo");
                registro[1]= rs.getString("profe_cedula");
                registro[2]= rs.getString("profe_nombre");
                registro[3]= rs.getString("profe_apellido");
                registro[4]= rs.getString("profe_celular");
                registro[5]= rs.getString("profe_mail");
                registro[6]= rs.getString("profe_titulo");
                registro[7]= rs.getString("profe_estado");
                modelo.addRow(registro);
            }
            TablaProfe.setModel(modelo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostar los datos"+ e);
        }
    }


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String SQL="INSERT INTO profesor(profe_cedula,profe_nombre,profe_apellido,profe_celular,profe_mail,profe_titulo,profe_estado)values(?,?,?,?,?,?,?)";
            PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1,txtCedula.getText());
            pst.setString(2,txtNombre.getText());
            pst.setString(3,txtApellido.getText());
            pst.setString(4,txtCelular.getText());
            pst.setString(5,txtMail.getText());
            pst.setString(6,txtTitulo.getText());
            int  seleccion=ComEstado.getSelectedIndex();
            pst.setString(7, ComEstado.getItemAt(seleccion));
            //aqui ejecutamos la consulta
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro exitoso");
                 
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Error de insercion");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada=TablaProfe.getSelectedRow();
        try {
            String SQL= "delete from profesor where profe_codigo="+TablaProfe.getValueAt(filaSeleccionada, 0);
            Statement st=(Statement) con.createStatement();
            int n=st.executeUpdate(SQL);
            if(n>=0){
                JOptionPane.showMessageDialog(null, "Registro Eliminado correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error en eliminar registro"+e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


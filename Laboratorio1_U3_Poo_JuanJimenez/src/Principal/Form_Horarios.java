    public void mostrarDatos(){
        String titulos[]={"Codigo","Hora","Turno","Disponibilidad"};
        String registro[]=new String [4];
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        String SQL="select * from horario";
        try{
            Statement st =(Statement) con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            while(rs.next()){
                registro[0]= rs.getString("hora_codigo");
                registro[1]= rs.getString("hora_hora");
                registro[2]= rs.getString("hora_turno");
                registro[3]= rs.getString("hora_dispo");
                modelo.addRow(registro);
            }
            tablaHorario.setModel(modelo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostar los datos"+ e);
        }
    }
    
    

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String SQL="INSERT INTO horario(hora_hora,hora_turno,hora_dispo)values(?,?,?)";
            PreparedStatement pst=(PreparedStatement) con.prepareStatement(SQL);
            pst.setString(1, txtHora.getText());
            int seleccion=ComTurno.getSelectedIndex();
            pst.setString(2,ComTurno.getItemAt(seleccion));
            int seleccion2=ComDispo.getSelectedIndex();
            pst.setString(3,ComDispo.getItemAt(seleccion2));
            //aqui ejecutamos la consulta
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro exitoso");
                 
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Error de insercion");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

